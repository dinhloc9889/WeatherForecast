package com.example.weatherapp;

public class ThoiTiet {
    public String ngayThang;
    public String trangThai;
    public String hinh;
    public String nhietDoCao;
    public String nhietDoThap;
    public String doCF;

    public ThoiTiet(String ngaythang, String trangThai, String hinh, String nhietDoCao, String nhietDoThap, String doCF) {
        this.ngayThang = ngaythang;
        this.trangThai = trangThai;
        this.hinh = hinh;
        this.nhietDoCao = nhietDoCao;
        this.nhietDoThap = nhietDoThap;
        this.doCF = doCF;
    }
}
