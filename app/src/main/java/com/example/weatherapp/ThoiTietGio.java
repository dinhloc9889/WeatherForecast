package com.example.weatherapp;

public class ThoiTietGio {
    public String nhietDoGio;
    public String hinhGio;
    public String trangThaiGio;
    public String gioCapNhat;
    public String doCF;

    public ThoiTietGio(String nhietDoGio, String hinhGio, String trangThaiGio, String gioCapNhat, String doCF) {
        this.nhietDoGio = nhietDoGio;
        this.hinhGio = hinhGio;
        this.trangThaiGio = trangThaiGio;
        this.gioCapNhat = gioCapNhat;
        this.doCF = doCF;
    }
}
